#pragma mark - Push

- (void)requestPushPermission:(JS::NativePyrxSynapse::PushPermissionOptions &)options
                      resolve:(RCTPromiseResolveBlock)resolve
                       reject:(RCTPromiseRejectBlock)reject {
  // Default each flag to true (matches the iOS SDK's
  // requestPushPermission default and the typical FCM flow on Android).
  BOOL alert = options.alert().has_value() ? options.alert().value() : YES;
  BOOL sound = options.sound().has_value() ? options.sound().value() : YES;
  BOOL badge = options.badge().has_value() ? options.badge().value() : YES;
  [[PyrxSynapseImpl shared] requestPushPermissionWithAlert:alert
                                                     sound:sound
                                                     badge:badge
                                                  resolver:resolve
                                                  rejecter:reject];
}

- (void)getPushPermissionStatus:(RCTPromiseResolveBlock)resolve
                         reject:(RCTPromiseRejectBlock)reject {
  [[PyrxSynapseImpl shared] getPushPermissionStatusWithResolver:resolve rejecter:reject];
}

