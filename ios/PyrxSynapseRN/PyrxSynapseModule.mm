/*
 * PyrxSynapseModule.mm
 * @pyrx/synapse-react-native — iOS TurboModule bridge implementation.
 *
 * Glue between the codegen-generated `NativePyrxSynapseSpec` protocol
 * and the Swift `PyrxSynapseImpl` class (which is where the real work
 * happens — see PyrxSynapseImpl.swift).
 *
 * Pattern overview
 * ----------------
 * Every protocol method here does the same three things:
 *   1. Unbox the ObjC types the codegen handed us into Swift-friendly
 *      types (NSString → String, NSNumber → Bool/Int/Double, etc.).
 *   2. Call the corresponding method on `[PyrxSynapseImpl shared]`,
 *      forwarding the RN promise resolver/rejecter closures.
 *   3. The Swift side fulfills the resolver on success or invokes the
 *      rejecter with a typed `code` string + message on failure.
 *
 * Error code contract
 * -------------------
 * The Swift side maps every PyrxError case to one of:
 *   "not_initialized" | "permission_denied" | "network_error" |
 *   "invalid_argument" | "internal_error"
 * The JS surface (`src/index.ts`, PR-2) translates these into the
 * SynapseError class.
 *
 * Why we don't expose `handleDeviceToken` to JS
 * --------------------------------------------
 * APNs registration callbacks fire on the AppDelegate, before the JS
 * bridge is alive on cold-start launches. The base class
 * `PYRXSynapseAppDelegate` (PyrxSynapseAppDelegate.swift in this same
 * directory) intercepts those callbacks and forwards them to the
 * underlying `Pyrx.shared.handleDeviceToken` automatically. The JS
 * layer never sees the token — it just observes the resulting
 * device-registered telemetry via the dashboard.
 */

#import "PyrxSynapseModule.h"
// Forward declaration of the Swift impl class. The bridge header
// (generated by SPM/Xcode as `PyrxSynapseRN-Swift.h`) defines the real
// signatures.
#if __has_include("PyrxSynapseRN-Swift.h")
#import "PyrxSynapseRN-Swift.h"
#else
#import <PyrxSynapseRN/PyrxSynapseRN-Swift.h>
#endif

@implementation PyrxSynapseModule

RCT_EXPORT_MODULE()

#pragma mark - Lifecycle

- (void)initialize:(JS::NativePyrxSynapse::SynapseInitConfig &)config
           resolve:(RCTPromiseResolveBlock)resolve
            reject:(RCTPromiseRejectBlock)reject {
  // Unbox the codegen struct into a plain NSDictionary the Swift side
  // can iterate at runtime. Optional fields (`baseUrl`, `logLevel`,
  // `maxQueueSize`) come through as nil when omitted by the JS caller.
  NSMutableDictionary *cfg = [NSMutableDictionary new];
  cfg[@"workspaceId"] = config.workspaceId();
  cfg[@"apiKey"] = config.apiKey();
  cfg[@"environment"] = config.environment();
  if (config.baseUrl().has_value()) {
    cfg[@"baseUrl"] = config.baseUrl().value();
  }
  if (config.logLevel().has_value()) {
    cfg[@"logLevel"] = config.logLevel().value();
  }
  if (config.maxQueueSize().has_value()) {
    cfg[@"maxQueueSize"] = @(config.maxQueueSize().value());
  }
  [[PyrxSynapseImpl shared] initializeWithConfig:cfg resolver:resolve rejecter:reject];
}

- (void)setLogLevel:(NSString *)level
            resolve:(RCTPromiseResolveBlock)resolve
