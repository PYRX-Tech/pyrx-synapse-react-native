/*
 * PyrxSynapseModule.mm
 * @pyrx/synapse-react-native ‚Äî iOS TurboModule bridge implementation.
 *
 * Glue between the codegen-generated `NativePyrxSynapseSpec` protocol
 * and the Swift `PyrxSynapseImpl` class (which is where the real work
 * happens ‚Äî see PyrxSynapseImpl.swift).
 *
 * Pattern overview
 * ----------------
 * Every protocol method here does the same three things:
 *   1. Unbox the ObjC types the codegen handed us into Swift-friendly
 *      types (NSString ‚Üí String, NSNumber ‚Üí Bool/Int/Double, etc.).
 *   2. Call the corresponding method on `[PyrxSynapseImpl shared]`,
 *      forwarding the RN promise resolver/rejecter closures.
 *   3. The Swift side fulfills the resolver on success or invokes the
 *      rejecter with a typed `code` string + message on failure.
 *
 * Error code contract
 * -------------------
 * The Swift side maps every PyrxError case to one of:
 *   "not_initialized" | "permission_denied" | "network_error" |
 *   "invalid_argument" | "internal_error"
 * The JS surface (`src/index.ts`, PR-2) translates these into the
 * SynapseError class.
 *
 * Why we don't expose `handleDeviceToken` to JS
 * --------------------------------------------
 * APNs registration callbacks fire on the AppDelegate, before the JS
 * bridge is alive on cold-start launches. The base class
 * `PYRXSynapseAppDelegate` (PyrxSynapseAppDelegate.swift in this same
 * directory) intercepts those callbacks and forwards them to the
 * underlying `Pyrx.shared.handleDeviceToken` automatically. The JS
 * layer never sees the token ‚Äî it just observes the resulting
 * device-registered telemetry via the dashboard.
 *
 * NativeEventEmitter integration (Phase 9.2.1)
 * --------------------------------------------
 * This class extends `RCTEventEmitter` so JS subscribers using
 * `new NativeEventEmitter(NativePyrxSynapse)` receive events from the
 * native `Pyrx.shared.events()` AsyncStream. The actual subscription
 * is owned by `PyrxSynapseImpl` (Swift); we install a back-reference
 * to `self` (the emitter) in `startObserving` so the impl can call
 * `sendEventWithName:body:` for each PyrxEvent received from the SDK.
 *
 * Lifecycle:
 *   - JS calls `new NativeEventEmitter(NativePyrxSynapse)`. The first
 *     `addListener` triggers `startObserving` ‚Üí we plug the emitter
 *     into the Swift impl and the impl starts collecting the SDK
 *     AsyncStream into a held `Task`.
 *   - The Swift impl forwards each event by calling
 *     `sendEventWithName:body:` on this emitter (via the back-ref).
 *   - The last `removeListeners` decrement triggers `stopObserving` ‚Üí
 *     we cancel the impl's collecting Task and clear the back-ref.
 *
 * Multi-bridge note: in dev with Metro fast-reload, the RN bridge can
 * be torn down and re-created. `RCTInvalidating` (which RCTEventEmitter
 * conforms to) calls `invalidate` on bridge teardown; we forward to
 * the impl so its held Task is cancelled before a new bridge instance
 * attaches.
 */

